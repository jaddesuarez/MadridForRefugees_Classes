// reverse() method
//  - It reverses the order of the elements present in the array.
// - IMPORTANT: It mutates the original array.

// Sample 1
const numbers = [1, 2, 3, 4, 5];

numbers.reverse();

console.log("Reversed numbers ==>", numbers);

console.log("------");

// Sample 2
const numbersString = ["one", "two", "three", "four", "five"];

numbersString.reverse();

console.log("Reversed numbersString ==>", numbersString);
